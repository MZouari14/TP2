def somme_harmonique(n):
    if n == 1:
        return 1
    else:
        return 1 / n + somme_harmonique(n - 1)
###########################################################################################################################################################         
############################################################__Main__#######################################################################################
###########################################################################################################################################################  

n = 4
resultat = somme_harmonique(n)
print(f"La somme harmonique jusqu'au terme {n} est : {resultat}\n")

